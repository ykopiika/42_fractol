#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	char	*d;
	char	*s;

	d = s1;
	s = (char*)s2;
	while (*d != '\0')
		d++;
	while (*s != '\0')
		*d++ = *s++;
	*d = '\0';
	return (s1);
}
