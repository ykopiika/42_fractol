#include "libft.h"

char	*ft_strcpy(char *dst, const char *src)
{
	char	*s;
	char	*d;
	int		i;

	i = 0;
	s = (char*)src;
	d = dst;
	if (!src && !dst)
		return (NULL);
	while (s[i])
	{
		d[i] = s[i];
		i++;
	}
	d[i] = '\0';
	return (dst);
}
