#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	char	*d;
	char	*s;
	size_t	i;

	d = s1;
	s = (char*)s2;
	i = 0;
	while (*d != '\0')
		d++;
	while ((i < n) && *s)
	{
		*d++ = *s++;
		i++;
	}
	*d = '\0';
	return (s1);
}
